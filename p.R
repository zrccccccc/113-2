資料集概覽
這份資料集包含2002年至2024年間台灣國民出國旅遊的統計數據，按目的地分類，顯示各年度有多少台灣旅客前往不同國家與地區。這些數據可用來分析出國旅遊趨勢，以及各國受歡迎程度的變化。

資料集的變數
首站抵達地（First Arrival Destination） – 旅客最先抵達的大區域（如亞洲、歐洲、美洲）。
細分（Subcategory/Destination Country or Region） – 具體的國家或地區（如日本、韓國、香港）。
2002-2024（年份） – 該年台灣旅客前往該目的地的人數。
變數類型
首站抵達地（First Arrival Destination） – 類別型（Nominal）（例如：亞洲、美洲、歐洲）。
細分（Destination Country/Region） – 類別型（Nominal）（具體國家或地區名稱）。
年度數據（2002-2024） – 數值型（離散/連續）（每年出國人數）。
